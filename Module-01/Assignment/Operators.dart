//Declare two constant variables "a" and "b" with any integer values of your choice.
// Perform addition, subtraction, multiplication, division, and modulus directly on "a" and "b".
// Print the results of each operation to the console using appropriate messages.
void main(){

  const int a = 7;
  const int b = 3;

  int addResult = a + b;
  print('Addition:  $a + $b = $addResult');

  int subResult = a - b;
  print('Subtraction:  $a - $b = $subResult');

  int mulResult = a * b;
  print('Multiplication:  $a * $b = $mulResult');

  double divResult = a / b;
  print('Division:  $a / $b = $divResult');

  int modResult = a % b;
  print('Modulus:  $a % $b = $modResult');

}
